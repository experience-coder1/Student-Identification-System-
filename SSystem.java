public class Student{
    private int std_id;
    private  String std_name;
    private String std_address;

    public void set_std_id(int std_id){
        this.std_id = std_id;
    }
    public void set_std_name(String std_name){
        this.std_name = std_name;
    }
    public void set_std_address(String std_address){
        this.std_address = std_address;
    }
    public int get_std_id(){
        return std_id;
    }
    public String get_std_name(){
        return std_name;
    }
    public String get_std_address(){
        return std_address;
    }
 
}
